num1 = int(input("Enter First Number-> "))
num2 = int(input("Enter Second number-> "))

Sqr1 = num1 * num1
Sqr2 = num2 * num2

if Sqr1:
    print("Square of FirstNumber is -> ", Sqr1)

if Sqr2:
    print("Square of SecondNumber is -> ", Sqr2)

if Sqr1 + Sqr2:
    print("Sum of the squares is -> ", Sqr1 + Sqr2)

if Sqr1 - Sqr2:
    print("Subtraction of the squares is -> ", Sqr1 - Sqr2)